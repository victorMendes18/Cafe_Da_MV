package br.com.cafe;

import java.util.Date;

public class cadastro {
	private String nome;
	private String cpf;
	private String opçao;
	
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	public String getCpf() {
		return cpf;
	}
	public void setCpf(String cpf) {
		this.cpf = cpf;
	}
	public String getOpçao() {
		return opçao;
	}
	public void setOpçao(String opçao) {
		this.opçao = opçao;
	}
	
}
